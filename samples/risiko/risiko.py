class RisikoDataset(utils.Dataset):
    def load_dataset(self, dataset_dir, subset):
        """Load a subset of the Balloon dataset.
        dataset_dir: Root directory of the dataset.
        subset: Subset to load: train or val
        """
        # Load annotations
        # VGG Image Annotator (up to version 1.6) saves each image in the form:
        # { 'filename': '28503151_5b5b7ec140_b.jpg',
        #   'regions': {
        #       '0': {
        #           'region_attributes': {},
        #           'shape_attributes': {
        #               'all_points_x': [...],
        #               'all_points_y': [...],
        #               'name': 'polygon'}},
        #       ... more regions ...
        #   },
        #   'size': 100202
        # }
        # We mostly care about the x and y coordinates of each region
        # Note: In VIA 2.0, regions was changed from a dict to a list.


        # Add classes. We have only one class to add.
        self.add_class("risiko", 1, "infantry_blue")
        self.add_class("risiko", 2, "horse_blue")
        self.add_class("risiko", 3, "cannon_blue")

        self.add_class("risiko", 4, "infantry_green")
        self.add_class("risiko", 5, "horse_green")
        self.add_class("risiko", 6, "cannon_green")

        # Train or validation dataset?
        assert subset in ["train", "val"]
        dataset_dir = os.path.join(dataset_dir, subset)

        annotations = json.load(open(os.path.join(dataset_dir, "risiko.json")))
        annotations = annotations.get("_via_img_metadata")
        annotations = list(annotations.values())

        for a in annotations:
            polygons, objects = self._get_polygons_and_objects(a)

            num_ids = []
            for region_attrs in objects:
                unit_type = region_attrs.get("Type")
                unit_color = region_attrs.get("Color")

                if unit_type == "Infantry" and unit_color == "Blue":
                    num_ids.append(1)
                    pass
                elif unit_type == "Horse" and unit_color == "Blue":
                    num_ids.append(2)
                    pass
                elif unit_type == "Cannon" and unit_color == "Blue":
                    num_ids.append(3)
                    pass
                elif unit_type == "Infantry" and unit_color == "Green":
                    num_ids.append(4)
                    pass
                elif unit_type == "Horse" and unit_color == "Green":
                    num_ids.append(5)
                    pass
                elif unit_type == "Cannon" and unit_color == "Green":
                    num_ids.append(6)
                pass

            image_path = os.path.join(dataset_dir, a['filename'])
            image = skimage.io.imread(image_path)
            height, width = image.shape[:2]

            self.add_image(
                "risiko",
                image_id=a['filename'],  # use file name as a unique image id
                path=image_path,
                width=width, height=height,
                polygons=polygons,
                num_ids=num_ids)

    def _get_polygons_and_objects(self, annotation):
        polygons = []
        objects = []
        region_list = annotation.get("regions")

        for region in region_list:
            shape_attrs = region.get("shape_attributes")
            region_attrs = region.get("region_attributes")

            x = shape_attrs.get("x")
            y = shape_attrs.get("y")
            width = shape_attrs.get("width")
            height = shape_attrs.get("height")
            # print("X: {} | Y: {} | W: {} | H: {}".format(x, y, width, height))

            all_points_x = (x, x, x + width, x + width)
            all_points_y = (y, y + height, y + height, y)
            polygon = {"name": "polygon",
                       "all_points_x": all_points_x,
                       "all_points_y": all_points_y}

            polygons.append(polygon)
            objects.append(region_attrs)
            # print("Polygon: {}".format(polygon))
            # print("Objects: {}".format(region_attrs))
            # print("\n")
        return polygons, objects

    def _get_polygons_and_objects_orig(self, annotation):
        return [r['shape_attributes'] for r in annotation['regions']], [s['region_attributes'] for s in annotation['regions']]

    def load_mask(self, image_id):
        """Generate instance masks for an image.
       Returns:
        masks: A bool array of shape [height, width, instance count] with
            one mask per instance.
        class_ids: a 1D array of class IDs of the instance masks.
        """
        # If not a balloon dataset image, delegate to parent class.
        image_info = self.image_info[image_id]
        if image_info["source"] != "risiko":
            return super(self.__class__, self).load_mask(image_id)

        # Convert polygons to a bitmap mask of shape
        # [height, width, instance_count]
        info = self.image_info[image_id]
        mask = np.zeros([info["height"], info["width"], len(info["polygons"])],
                        dtype=np.uint8)
        for i, p in enumerate(info["polygons"]):
            # Get indexes of pixels inside the polygon and set them to 1
            rr, cc = skimage.draw.polygon(p['all_points_y'], p['all_points_x'])
            mask[rr, cc, i] = 1

        # Return mask, and array of class IDs of each instance. Since we have
        # one class ID only, we return an array of 1s
        return mask.astype(np.bool), np.ones([mask.shape[-1]], dtype=np.int32)

    def image_reference(self, image_id):
        # TODO: Override
        info = self.image_info[image_id]
        if info["source"] == "risiko":
            return info["path"]
        else:
            super(self.__class__, self).image_reference(image_id)


